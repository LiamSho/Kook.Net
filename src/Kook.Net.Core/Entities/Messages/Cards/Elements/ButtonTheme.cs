namespace Kook;

/// <summary>
///     Specifies the theme of a <see cref="ButtonElement"/>.
/// </summary>
public enum ButtonTheme
{
    /// <summary>
    ///     The button is a primary button.
    /// </summary>
    Primary,
    /// <summary>
    ///     The button is a success button.
    /// </summary>
    Success,
    /// <summary>
    ///     The button is a warning button.
    /// </summary>
    Warning,
    /// <summary>
    ///     The button is a danger button.
    /// </summary>
    Danger,
    /// <summary>
    ///     The button is an info button.
    /// </summary>
    Info,
    /// <summary>
    ///     The button is a secondary button.
    /// </summary>
    Secondary,
}
