namespace Kook;

/// <summary>
///     Specifies the type of games.
/// </summary>
public enum GameType
{
    /// <summary>
    ///     Specifies that the game is just a game.
    /// </summary>
    Game = 0,

    /// <summary>
    ///     Specifies that the game is a virtual uploader.
    /// </summary>
    VirtualUploader = 1,

    /// <summary>
    ///     Specifies that the game is a process.
    /// </summary>
    Process = 2
}
